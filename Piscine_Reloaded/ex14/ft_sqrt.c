int	ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	if (nb == 1)
		return (sqrt);
	while (sqrt <= nb / sqrt)
	{
		if (nb == sqrt * sqrt)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
