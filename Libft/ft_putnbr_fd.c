#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (nb < 0 && nb > -10)
	{
		ft_putchar_fd('-', fd);
	}
	if (nb >= 10 || nb <= -10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd((nb % 10 < 0) ? (-(nb % 10)) : (nb % 10), fd);
	}
	else
		ft_putchar_fd('0' + ((nb % 10 < 0) ? (-(nb % 10)) : (nb % 10)), fd);
}
